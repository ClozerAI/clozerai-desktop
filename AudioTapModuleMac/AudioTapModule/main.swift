func startCapture() async throws {
    guard let content = shareableContent,
            let display = content.displays.first else {
        debugPrint("No display found â€“ cannot start capture.")
        exit(1)
    }

    let filter = SCContentFilter(display: display,
                                    excludingApplications: [],
                                    exceptingWindows: [])

    let cfg = SCStreamConfiguration()
    cfg.capturesAudio               = true
    cfg.excludesCurrentProcessAudio = false
    cfg.sampleRate                  = 16_000
    cfg.channelCount                = 1

    let stream = SCStream(filter: filter,
                            configuration: cfg,
                            delegate: nil)

    let sink   = AudioSink()
    try stream.addStreamOutput(sink,
                                type: .audio,
                                sampleHandlerQueue: DispatchQueue(label: "SCKitAudioCapture"))

    // ðŸ”´ Hold the objects globally so they outlive this function
    captureStream = stream
    audioSink     = sink

    debugPrint("INFO: Starting ScreenCaptureKit audio stream â€¦")
    try await stream.startCapture()
}

